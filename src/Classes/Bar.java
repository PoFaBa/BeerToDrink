package Classes;

public class Bar {

	private int IdBar;
	private String AdresseBar;
	private String NomBar;
	private double Note;
	
	
	public int getIdBar() {
		return IdBar;
	}
	public void setIdBar(int idBar) {
		IdBar = idBar;
	}
	public String getAdresseBar() {
		return AdresseBar;
	}
	public void setAdresseBar(String adresseBar) {
		AdresseBar = adresseBar;
	}
	public String getNomBar() {
		return NomBar;
	}
	public void setNomBar(String nomBar) {
		NomBar = nomBar;
	}
	public double getNote() {
		return Note;
	}
	public void setNote(double note) {
		Note = note;
	}
	
}
