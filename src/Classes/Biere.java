package Classes;

public class Biere {
	
	private int IdBiere;
	private String NomBiere;
	private String TypeBiere;
	private double AlcBiere;
	
	
	public int getIdBiere() {
		return IdBiere;
	}
	public void setIdBiere(int idBiere) {
		IdBiere = idBiere;
	}
	public String getNomBiere() {
		return NomBiere;
	}
	public void setNomBiere(String nomBiere) {
		NomBiere = nomBiere;
	}
	public String getTypeBiere() {
		return TypeBiere;
	}
	public void setTypeBiere(String typeBiere) {
		TypeBiere = typeBiere;
	}
	public double getAlcBiere() {
		return AlcBiere;
	}
	public void setAlcBiere(double alcBiere) {
		AlcBiere = alcBiere;
	}
	
	
	
	

}
